#!/bin/bash

# エラーが発生したら即座にスクリプトを終了する
set -e

echo "=== .js ファイルの自動検証を開始します ==="

# 1階層下のディレクトリにあるすべての.jsファイルを対象ループ
for file in */*.js; do
  # ファイルが存在するか確認（マッチするファイルがない場合への対策）
  if [ -f "$file" ]; then
    echo "実行中: $file"
    # 入力を要求された場合のために自動で「1」を入力し続ける
    yes "1234" | node "$file"
  fi
done

echo "=== すべてのエラーチェックが完了しました ==="
